#Vous devez écrire un programme qui permet de déterminer IMC d'un individu en fonction de sa taille et de son poids.
#Catégorie 1 : Poids insuffisant (IMC < 18.5)
#Catégorie 2 : Poids normal (18.5 ≤ IMC < 25)
#Catégorie 3 : Surpoids (25 ≤ IMC < 30)
#Catégorie 4 : Obésité modérée (30 ≤ IMC < 35)
#Catégorie 5 : Obésité sévère (IMC ≥ 35) 
#Le programme doit demander à l'utilisateur de saisir sa taille en mètres et son poids en kilogrammes
#L'IMC est calculé en divisant le poids en kilogrammes par le carré de la taille en mètres (IMC = poids / (taille * taille)).


